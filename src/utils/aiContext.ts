export function buildFullSystemPrompt(rawData: any[], extraContext?: string): string {
  const parts = [
    '你是众引传播集团的资深媒介总监，精通 KOL 采买、返点谈判、团队 KPI 管理和数据分析。',
    TERMINOLOGY_CONTEXT,
    DATA_SCHEMA_CONTEXT,
    BUSINESS_RULES_CONTEXT,
  ];

  if (extraContext) {
    parts.push(`当前页面数据上下文：\n${extraContext}`);
  }

  if (rawData && rawData.length > 0) {
    // 计算数据概览
    const platforms = new Set(rawData.map(r => r['发布平台']).filter(Boolean));
    const customers = new Set(rawData.map(r => r['对客项目客户名称']).filter(Boolean));
    const brands = new Set(rawData.map(r => r['投放品牌']).filter(Boolean));
    const applicants = new Set(rawData.map(r => r['排期申请人']).filter(Boolean));

    const dates = rawData
      .map(r => r['下单日期'])
      .filter(Boolean)
      .map((d: any) => {
        if (d instanceof Date) return d;
        const parsed = new Date(d);
        return isNaN(parsed.getTime()) ? null : parsed;
      })
      .filter(Boolean) as Date[];

    const validDates = dates.sort((a, b) => a.getTime() - b.getTime());
    const startDate = validDates[0]?.toISOString().split('T')[0] || '-';
    const endDate = validDates[validDates.length - 1]?.toISOString().split('T')[0] || '-';

    const totalAmount = rawData.reduce((sum, r) => sum + (Number(r['Kol刊例单价(元)']) || 0), 0);
    const totalNet = rawData.reduce((sum, r) => sum + (Number(r['kol净价(元)']) || 0), 0);
    const avgRebate = totalAmount > 0 ? ((totalAmount - totalNet) / totalAmount) * 100 : 0;

    parts.push(
      `当前上传数据概览：共 ${rawData.length} 条记录，` +
      `覆盖 ${platforms.size} 个平台、` +
      `${customers.size} 家客户、` +
      `${brands.size} 个品牌、` +
      `${applicants.size} 名媒介人员。` +
      `时间范围：${startDate} 至 ${endDate}。` +
      `总刊例额 ¥${(totalAmount / 10000).toFixed(1)} 万，整体返点率 ${avgRebate.toFixed(1)}%。`
    );
  } else {
    parts.push('当前看板中尚未上传数据，请引导用户先上传包含"KOL明细数据"工作表的 Excel 文件。');
  }

  parts.push(
    '请基于以上知识和数据，用专业、准确、可操作的语言回答用户问题。' +
    '在引用数据时请注明统计口径。如果用户问题需要精确数据而你无法直接计算，请说明需要查询的数据维度。'
  );

  return parts.join('\n\n');
}
